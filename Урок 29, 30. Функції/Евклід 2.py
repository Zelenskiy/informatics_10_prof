'''
21 14
7
НСД (найб. сп. дільник) Евкліда

21 14
7  14
<->
14 7
0  7
<->
7 0
GCD = 7

c = a
a = b
b = c


'''
a = int(input("a = "))
b = int(input("b = "))

while (b !=0):
    a = a % b    #Залишок від ділення a на b a %= b
    a, b = b, a  #Обмінюємо значення змінних a та b
    

print("GCD  =", a)
